    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        int i = tabel.getSelectedRow();
        kodefilm.setText(model.getValueAt(i, 0).toString());
        
        try {
            this.film.updatefilm(Integer.parseInt(harga.getText()),kodefilm.getText());
            this.showTableFilm();
        } catch (SQLException ex) {
            Logger.getLogger(MANAGEFILM.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        int i = tabel.getSelectedRow();
        Integer film = Integer.parseInt(model.getValueAt(i, 0).toString());
        try {
            this.film.deletefilm(film);
            this.showTableFilm();
        } catch (SQLException ex) {
            Logger.getLogger(MANAGEFILM.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_deleteActionPerformed

